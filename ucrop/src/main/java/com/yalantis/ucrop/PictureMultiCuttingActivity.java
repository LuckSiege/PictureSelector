        final Intent intent = getIntent();
        list = getIntent().getStringArrayListExtra(UCropMulti.Options.EXTRA_CUT_CROP);
        // Crop cut list
        if (list != null && list.size() > 0) {
            CutInfo cutInfo;
            for (String path : list) {
                cutInfo = new CutInfo(path, false);
                cutInfos.add(cutInfo);
            }
            initData();
        }
